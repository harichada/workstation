<h1 id="community-translation-policy-for-the-linux-command-line">Community Translation Policy for <em>The Linux Command Line</em></h1>
<p>I get a lot of requests from people who want to translate <em>The Linux Command Line</em>. Up to now, I have been in a quandary as to a policy to facilitate this.</p>
<p>As you know, the license under which the book is released is a <a href="http://creativecommons.org/licenses/by-nc-nd/3.0/">Creative Commons Attribution-Noncommercial-No Derivative Works License</a>. This means that readers are free to copy and distribute the book in its original form but are not permitted to sell copies, nor are they allowed to make changes to the work. Without permission from the author, translations are not allowed, since a translation is a derivative work.</p>
<p>When I set out to write <em>The Linux Command Line</em>, I wanted to do something to pay back the Linux community for all of the free software I have been using all these years, and I want my book to be available to as wide an audience as possible, so translations are a good thing.</p>
<p>My publishing agreement with No Starch Press, however, makes this situation a little complicated as they have exclusive rights to certain formats, and I don't want my efforts to compete with theirs.</p>
<p>If you would like to create a translated version of <em>The Linux Command Line</em>, please contact me and I will grant permission, provided you agree to the following restrictions:</p>
<ol style="list-style-type: decimal">
<li><p><strong>Allowable Formats -</strong> My publisher has exclusive rights to all formats except HTML and PDF. This means that translations must be distributed only in HTML and/or PDF. Distribution of printed copies is not permitted. The same goes for ePub, .mobi, etc.</p></li>
<li><p><strong>Completeness -</strong> If you undertake the task of translating the book, you must translate the entire text, not just parts of it. You don't have to include an index (though it would be nice if you did). Please understand that translating the entire text is a lot of work and that you should not undertake the effort unless you are prepared to complete it.</p></li>
<li><p><strong>No Censorship -</strong> I realize that certain passages in the book are mildly political regarding things like freedom, secrecy, free expression, etc. If you translate the book, you must not change the text in any way that compromises the integrity of the work.</p></li>
<li><p><strong>Source Materials -</strong> The only source material for translation is the PDF version of the book. No other format is available.</p></li>
<li><p><strong>Copyright Notice -</strong> Translated versions of the book must include the the original copyright notice and licensing information found in the PDF version of the book. You should include your own your own name and email address as an addendum to the copyright notice, indicating that you are responsible for the translation.</p></li>
<li><p><strong>Distribution -</strong> You are responsible for distribution of your translated version. Unfortunately, I cannot provide hosting for you. I will, however, happily display a link on LinuxCommand.org to your translation.</p></li>
<li><p><strong>No Sales -</strong> Translated versions of <em>The Linux Command Line</em> must be distributed free-of-charge.</p></li>
</ol>


