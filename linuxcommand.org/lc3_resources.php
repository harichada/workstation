<h2>Other Sites You Might Enjoy</h2>

<h3>Bash And Scripting</h3>
<ul>
	<li><p><b><a href="http://www.gnu.org/software/bash/manual/bash.html">The Bash Reference Manual</a></b></p>
	
	<p>Perhaps in response to the usability issues found
	 in the bash man page, the <a href="http://www.gnu.org/">GNU Project</a> produced the 
	 Bash Reference Manual.  You can think of it as the 
	 bash man page translated into human readable form.  
	 While it lacks a tutorial focus and contains no usage
	 examples, it is much easier to read and is more 
	 usefully organized than the bash man page.</p>
	</li>
	
	<li><p><b><a href="http://mywiki.wooledge.org/">Greg's Wiki</a></b></p>
	
	<p>The bash man page and the Bash Reference Manual 
	both extensively document the features found in bash.
	However, when we want a description of bash behavior,
	different resources are needed.  The best by far is
	Greg's Wiki.  This site covers a variety of topics, 
	but of particular interest to us are the <a href="http://mywiki.wooledge.org/BashFAQ">Bash FAQ</a> which 
	contains over one hundred frequently asked questions 
	about bash, the <a href="http://mywiki.wooledge.org/BashPitfalls">Bash Pitfalls</a> which describes many of 
	the common problems script writers encounter with bash, 
	and the <a href="http://mywiki.wooledge.org/BashGuide">Bash Guide</a>, a useful set of tutorials for bash
	users.  There are also several fun to read <a href="http://mywiki.wooledge.org/CategoryRant">rants</a>. </p>
	</li>
	
	<li><p><b><a href="http://wiki.bash-hackers.org/start">Bash Hacker's Wiki</a></b></p>
	
	<p>Like Greg's Wiki, the Bash Hackers Wiki provides many 
	different articles relating to bash, its features, and
	its behavior.  Included are some useful tutorials on 
	various programming techniques and issues with scripting
	with bash.  While the writing is, at times, a little
	chaotic, it does contain useful information.</p>
	</li>
	
	<li><p><b><a href="http://tiswww.case.edu/php/chet/bash/bashtop.html">Chet Ramey's Bash Page</a></b></p>
	
	<p>Chet Ramey is the current maintainer of bash and he has 
	his own page.  On it, you can find version information, 
	latest news, and other things.  The most useful document 
	on the Bash Page is its version of the <a href="http://tiswww.case.edu/php/chet/bash/FAQ">Bash FAQ</a>.  The 
	<a href="http://tiswww.case.edu/php/chet/bash/NEWS">NEWS</a> 
	file contains a concise list of features that have been 
	added to each version of bash.</p>
	</li>
	
	<li><p><b><a href="http://www.shelldorado.com/">Heiner's SHELLdorado</a></b></p>
	
	<p>SHELLdorado is a shell scripting education site. It features 
	tutorials on various scripting topics and a large collection 
	of shell scripts and functions.</p>
	</li>
	
	<li><p><b><a href="http://www.tldp.org/LDP/abs/html/index.html">Advanced Bash Scripting HOWTO</a></b></p>
	
	<p>The Advanced Bash Scripting HOWTO is a detailed look at 
	bash shell programming. A thoughtful treatment with lots of 
	examples and exercises. A good companion piece for the 
	LinuxCommand.org tutorials.</p>
	</li>
	
	<li><p><b><a href="http://www.tldp.org/HOWTO/Bash-Prompt-HOWTO/index.html">The Bash Prompt HOWTO</a></b></p>
	
	<p>The Bash Prompt HOWTO is a surprisingly interesting 
	discussion of all the things you can make the prompt do.</p>
	</li>
	
	<li><p><b><a href="http://www.linuxfromscratch.org/">Linux From Scratch</a></b></p>
	
	<p>The Linux From Scratch tutorial will step you through 
	the process of building a complete Linux system from source 
	code.</p>
	</li>
	</ul>
	
	<h3>Linux Standards</h3>
	
	<ul>
	<li><p><b><a href="http://www.pathname.com/fhs/">The Linux Filesystem Hierarchy Standard</a></b></p>
	
	<p>The Linux Filesystem Hierarchy Standard describes the 
	standard layout of a Linux system. A good site to visit if 
	you want to learn how and why Linux is organized the way 
	it is.</p>
	</li>
	
	<li><p><b><a href="http://www.linuxbase.org/">Linux Standard Base</a></b></p>
	
	<p>Linux Standard Base is a project devoted to developing a 
	standard for Linux distributions.</p>
	</li>
	</ul>
	
	<h3>Fighting For Your Freedom</h3>
	
	<ul>
	<li><p><b><a href="http://www.fsf.org/">Free Software Foundation</a></b></p>
	
	<p>The Free Software Foundation (FSF) is dedicated to 
	promoting computer users' right to use, study, copy, 
	modify, and redistribute computer programs.</p>
	</li>
	
	<li><p><b><a href="http://www.eff.org/">Electronic Frontier Foundation</a></b></p>
	
	<p>EFF is working to protect our fundamental rights regardless 
	of technology, to educate the press, policymakers and the 
	general public about civil liberties issues related to 
	technology, and to act as a defender of those liberties.</p>
	</li>
	
	<li><p><b><a href="http://www.softwarefreedom.org/">Software Freedom Law Center</a></b></p>
	
	<p>SFLC provides legal representation and other law-related 
	services to protect and advance Free and Open Source Software. 
	Founded in 2005, the Center now represents many of the most 
	important and well-established free software and open source 
	projects.</p>
	</li>
	
	<li><p><b><a href="http://www.againstmonopoly.org/">Against Monopoly</a></b></p>
	
	<p>Against Monopoly is a blog featuring writings by economists 
	and game theorists opposing various "intellectual property" 
	concepts. Be sure to read the free, downloadable book <i><a href="http://www.dklevine.com/general/intellectual/againstfinal.htm">Against 
	Intellectual Monopoly</a></i> by Michele Boldrin and David K. Levine.</p>
	</li>
</ul>


